    public void roomDetails() //method to enter room details
    {
        roomNumber.removeAllItems(); //changes according to the bed type when we clear
        roomPrice.setText("");
        bedType = (String)beds.getSelectedItem();
        roomType = (String)type.getSelectedItem();
        try
        {
            ResultSet rs = Select.getData("select * from room where bedType ='"+bedType+"'and roomType ='"+roomType+"'and status = 'Not Booked'");
        while(rs.next())
        {
            roomNumber.addItem(rs.getString(1));
        }
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }

