    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int id = 1;
        String name = customername.getText();
        String phoneNumber = customerNumber.getText();
        String nationality = cusnat.getText();
        String gender = (String)cusgender.getSelectedItem();
        String email = cusemail.getText();
        String idproof = cusid.getText();
        String  address = cusaddress.getText();
        String  checkIn = checkdate.getText();
        String bedType = (String)beds.getSelectedItem();
        String roomType = (String)type.getSelectedItem();
        String roomNo = (String)roomNumber.getSelectedItem();
        String price = roomPrice.getText();
        String Query ="select max(id)from customers";
                        
        try
        { 
            ResultSet rs = Select.getData(Query);
            while(rs.next()) // no record means the while will not run
                id = rs.getInt(1);
            id = id+1;
             
            
            if(!price.equals(""))
            {
                Query = "update room set status = 'Booked' where roomNo = '"+roomNo+"'";
                InsertUpdateDelete.setData(Query, ""); // message is not visible
                Query = "insert into customers(id,name,phoneNumber,nationality,gender,email,idproof,address,checkIn,roomNo,bedType,roomType,pricePerday) values("+id+",'"+name+"','"+phoneNumber+"','"+nationality+"','"+gender+"','"+email+"','"+idproof+"','"+address+"','"+checkIn+"','"+roomNo+"','"+bedType+"','"+roomType+"','"+price+"')";
                InsertUpdateDelete.setData(Query,"Customer booking succesfull" );
                setVisible(false);
                new CheckInCustomer().setVisible(true);
               
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

